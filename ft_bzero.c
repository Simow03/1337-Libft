#include "libft.h"

void	*ft_bzero(void *ptr, size_t size)
{
	char	*p;
	size_t	i;

	p = (char *)ptr;
	i = 0;
	while (i < size)
	{
		p[i] = 0;
		i++;
	}
	return (ptr);
}
